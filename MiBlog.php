    <h2>CUENTO DE ASÍ HABLABA QUETZALCÓATL</h2>

    <p>En aquel tiempo eran muchos los que se retiraban a la soledad de los montes a meditar. Y eran muchos los que en el silencio de los volcanes nevados y de los pequeños valles ocultos, se buscaban a sí mismos.

Pero Quetzalcóatl les decía: <br> <br>

Aquellos que buscan el Silencio en la calma nunca lo encontrarán desnudo.Para desnudarlo hay que buscarlo entre el bullicio. Díganme: ¿De qué sirve a un corazón tener calma en medio de la soledad?, ¿No es como la calma que tiene un lago estancado en un valle? Mas, ¡Qué grande es un corazón cuando en medio de las dificultades y los problemas conserva la calma y se viste con el Silencio! <br><br>

Es como el torrente que resbala sereno por la ladera de la montaña.

Algunos van a lugares donde lo más que les molesta es el canto de un pajarillo.

O el sonido que arranca el viento a las hojas de los árboles.

Y piensan: Estoy tranquilo y sereno en este lugar, ya he alcanzado la calma y el Silencio es amigo de mi corazón. <br><br>

Pero cuando vienen al bullicio, sus pulsos se agitan y sus corazones se alteran y sus pensamientos chocan con violencia en sus frentes, y yo les preguntaría: ¿Dónde guardaron la calma?
</p>

